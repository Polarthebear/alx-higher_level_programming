#include "lists.h"

/**
 * insert_node - insert number into a singly sorted list
 * @head: pointer to head
 * @number: number being inserted
 * Return: 0 (Fail) or pointer to node (Sucess)
 */
listint_t *insert_node(listint_t **head, int number)
{
	listint_t *node = *head, *new;

	new = malloc(sizeof(listint_t));

	if (new == NULL)
		return (NULL);
	new->n = number;

	if (node == NULL || node->n >= number)
	{
		new->next = node;
		*head = new;
		return (new);
	}

	while (node && node->next && node->next->n < number)
		node = node->next;

	new->next = node->next;
	node->next = new;

	return (new);
}
